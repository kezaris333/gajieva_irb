	printf("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ –≤ –¥–µ—Å—è—Ç–∏—á–Ω–æ–π —Å–∏—Å—Ç–µ–º–µ");
	scanf("% d", &num);
